// JavaScript Comparison operators.

var X = 10 , Y = 15;

// Equal to ( == )
console.log(X == Y);

// Equal value and equal type ( === )
console.log(X === Y);

// Not equal ( != )
console.log(X != Y);

// Not equal value or not equal type ( !== )
console.log(X !== Y);

// Greater than ( > )
console.log(X > Y);

// Less than ( < )
console.log(X < Y);

// Greater than or equal to ( >= )
console.log(X >= Y);

// Less than or equal to ( <= )
console.log(X <= Y);

// And ( && )
if(X < Y && X == Y){
    console.log("amar voyos 18 hoise");
}


// Or ( || )
if(X < Y || X == Y){
    console.log("amar voyos 18 hoi nai");
}