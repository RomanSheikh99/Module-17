// javascript conditional operation

// if statement

var myPoketMoney = 90;
var pizzaPrice = 45;

if(myPoketMoney > pizzaPrice){
    console.log("i can buy the pizza");
}


// else statement

if(myPoketMoney > pizzaPrice *2){
    console.log("wow i can buy two pizza");
}
else{
    console.log("ok i am happy with one");
}

// else if statement

if(myPoketMoney > pizzaPrice *2){
    console.log("wow i can buy two pizza");
}
